#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char*copy;
	int i;

	i = 0;
	if ((copy = malloc(sizeof(char) * ft_strlen(s1) + 1)) == NULL)
		return (NULL);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
