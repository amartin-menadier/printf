#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned const char *tmp1;
	unsigned const char *tmp2;

	tmp1 = (unsigned const char *)s1;
	tmp2 = (unsigned const char *)s2;
	while (n)
	{
		if (*tmp1 != *tmp2)
			return (*tmp1 - *tmp2);
		tmp1++;
		tmp2++;
		n--;
	}
	return (0);
}
